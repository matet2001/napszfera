                            Felhasználó – választása szerint – az alábbi kellékszavatossági igényekkel élhet: kérhet kijavítást vagy kicserélést, kivéve, ha az ezek közül a Felhasználó által választott igény teljesítése lehetetlen vagy a vállalkozás számára más igénye teljesítéséhez képest aránytalan többletköltséggel járna. Ha a kijavítást vagy a kicserélést nem kérte, illetve nem kérhette, úgy igényelheti az ellenszolgáltatás arányos leszállítását vagy a hibát a vállalkozás költségére Felhasználó is kijavíthatja, illetve mással kijavíttathatja vagy – végső esetben – a szerződéstől is elállhat. Választott kellékszavatossági jogáról egy másikra is áttérhet, az áttérés költségét azonban Felhasználó viseli, kivéve, ha az indokolt volt, vagy arra a vállalkozás adott okot.
                        </p>
                        <br>
                        <p>8.3. Milyen határidőben érvényesítheti Felhasználó kellékszavatossági igényét?</p>
                        <br>
                        <p>
                            Felhasználó (ha fogyasztónak minősül) köteles a hibát annak felfedezése után haladéktalanul, de nem később, mint a hiba felfedezésétől számított kettő hónapon belül közölni. Ugyanakkor felhívjuk a figyelmét, hogy a szerződés teljesítésétől számított két éves (vállalkozás esetén 1 éves) elévülési határidőn túl kellékszavatossági jogait már nem érvényesítheti.
                        </p>
                        <br>
                        <p>8.4. Kivel szemben érvényesítheti kellékszavatossági igényét?</p>
                        <br>
                        <p>
                            Felhasználó a Szolgáltatóval szemben érvényesítheti kellékszavatossági igényét.
                        </p>
                        <br>
                        <p>8.5. Milyen egyéb feltétele van kellékszavatossági jogai érvényesítésének (amennyiben a Felhasználó fogyasztónak minősül)?</p>
                        <br>
                        <p>
                            A teljesítéstől számított hat hónapon belül a kellékszavatossági igénye érvényesítésének a hiba közlésén túl nincs egyéb feltétele, ha Felhasználó igazolja, hogy a terméket, illetve a szolgáltatást a webshopot üzemeltető vállalkozás nyújtotta. A teljesítéstől számított hat hónap eltelte után azonban már Felhasználó köteles bizonyítani, hogy a Felhasználó által felismert hiba már a teljesítés időpontjában is megvolt.
                        </p>
                    </li>
                    <br>
                    <li>
                        <strong>Termékszavatosság</strong>
                        <br>
                        <p>8.6. Milyen esetben élhet Felhasználó a termékszavatossági jogával?</p>
                        <p>Ingó dolog (termék) hibája esetén Felhasználó – választása szerint – kellékszavatossági vagy termékszavatossági igényt érvényesíthet.</p>
                        <br>
                        <p>8.7. Milyen jogok illetik meg Felhasználót termékszavatossági igénye alapján?</p>
                        <p>Termékszavatossági igényként Felhasználó kizárólag a hibás termék kijavítását vagy kicserélését kérheti.</p>
                        <br>
                        <p>8.8. Milyen esetben minősül a termék hibásnak?</p>
                        <p>A termék akkor hibás, ha az nem felel meg a forgalomba hozatalakor hatályos minőségi követelményeknek, vagy pedig, ha nem rendelkezik a gyártó által adott leírásban szereplő tulajdonságokkal.</p>
                        <br>
                        <p>8.9. Milyen határidőben érvényesítheti Felhasználó termékszavatossági igényét?</p>
                        <p>Termékszavatossági igényét Felhasználó a termék gyártó általi forgalomba hozatalától számított két éven (vállalkozás esetén 1 éven) belül érvényesítheti. E határidő elteltével e jogosultságát elveszti.</p>
                        <br>
                        <p>8.10. Kivel szemben és milyen egyéb feltétellel érvényesítheti termékszavatossági igényét?</p>
                        <p>Termékszavatossági igényét kizárólag az ingó dolog gyártójával vagy forgalmazójával szemben gyakorolhatja. A termék hibáját termékszavatossági igény érvényesítése esetén Felhasználónak kell bizonyítania.</p>
                        <br>
                        <p>8.11. A (forgalmazó) milyen esetben mentesül termékszavatossági kötelezettsége alól?</p>
                        <br>
                        <p>A gyártó (forgalmazó) kizárólag akkor mentesül termékszavatossági kötelezettsége alól, ha bizonyítani tudja, hogy:</p>
                        <br>
                        <ul class="list-disc pl-10">
                            <li>a terméket nem üzleti tevékenysége körében gyártotta, illetve hozta forgalomba, vagy</li>
                            <li>a hiba a tudomány és a technika állása szerint a forgalomba hozatal időpontjában nem volt felismerhető vagy</li>
                            <li>a termék hibája jogszabály vagy kötelező hatósági előírás alkalmazásából ered.</li>
                        </ul>
                        <br>
                        <p>A gyártónak (forgalmazónak) a mentesüléshez elegendő egy okot bizonyítania. Felhívom figyelmét, hogy ugyanazon hiba miatt kellékszavatossági és termékszavatossági igényt egyszerre, egymással párhuzamosan nem érvényesíthet. Termékszavatossági igényének eredményes érvényesítése esetén azonban a kicserélt termékre, illetve kijavított részre vonatkozó kellékszavatossági igényét a gyártóval szemben érvényesítheti.</p>
